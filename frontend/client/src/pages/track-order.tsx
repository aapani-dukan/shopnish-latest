import { useParams, useLocation } from "react-router-dom"; // тЬЕ useLocation рдЬреЛрдбрд╝рд╛ рдЧрдпрд╛
import { useQuery } from "@tanstack/react-query"; // тЬЕ tanstack/react-query рд╕реЗ import
import { apiRequest } from "../lib/queryclient"; // тЬЕ рд╕рд╛рдкреЗрдХреНрд╖ рдкрде
import { Card, CardContent, CardHeader, CardTitle } from "../components/ui/card"; // тЬЕ рд╕рд╛рдкреЗрдХреНрд╖ рдкрде
import { Badge } from "../components/ui/badge"; // тЬЕ рд╕рд╛рдкреЗрдХреНрд╖ рдкрде
import { Button } from "../components/ui/button"; // тЬЕ рд╕рд╛рдкреЗрдХреНрд╖ рдкрде
